/** Возвращает статичные подсказки для страницы (или общий фолбэк). */
export function getStaticTips(page?: string): Tip[] {
  if (page && STATIC_TIPS[page]) return STATIC_TIPS[page];
  return [DEFAULT_STATIC_TIP];
}
